public class Numeros {
	
	public static void main (String[] args) {
    long DistanciaIntergalactica;
    
    DistanciaIntergalactica = 124431453563L; /*Debe llevar una l al final para indicar que es un numero de tipo long */ 
    
    System.out.println(DistanciaIntergalactica);
    
    double DistanciaMicrocospica = 33.01d; /*Al igual que con los numeros de tipo long, tambien indicamos que es de tipo double */
    System.out.println(DistanciaMicrocospica);
	}
}

